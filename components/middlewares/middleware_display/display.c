static uint8_t fi(char c) { uint8_t u=(uint8_t)c; return(u>=32&&u<=126)?u-32:0; }

/* ── Public API ───────────────────────────────────── */

#define HW_PAGE_COUNT (BSP_OLED_HEIGHT / 8)
#define HW_FB_SIZE    (BSP_OLED_WIDTH * HW_PAGE_COUNT)

/*
 * Set to 0 if the module is mounted in the opposite portrait direction.
 * Clockwise maps logical (64x128) coordinates onto physical (128x64) memory as:
 *   physical_x = logical_y, physical_y = 63 - logical_x
 */
#define DISPLAY_ROTATE_CLOCKWISE 1

static uint8_t s_fb[HW_FB_SIZE];
static uint8_t s_shadow[HW_FB_SIZE];

static void logical_to_physical(int x, int y, int *px, int *py)
{
#if DISPLAY_ROTATE_CLOCKWISE
    *px = y;
    *py = BSP_OLED_HEIGHT - 1 - x;
#else
    *px = BSP_OLED_WIDTH - 1 - y;
    *py = x;
#endif
}

static void set_logical_pixel(int x, int y, bool on)
{
    if (x < 0 || x >= DISPLAY_WIDTH || y < 0 || y >= DISPLAY_HEIGHT) {
        return;
    }

    int px;
    int py;
    logical_to_physical(x, y, &px, &py);

    const size_t offset = (size_t)(py / 8) * BSP_OLED_WIDTH + (size_t)px;
    const uint8_t mask = (uint8_t)(1u << (py & 0x07));

    if (on) {
        s_fb[offset] |= mask;
    } else {
        s_fb[offset] &= (uint8_t)~mask;
    }
}

static void put_logical_page_byte(int page, int column, uint8_t bits)
{
    const int y = page * 8;

    for (int bit = 0; bit < 8; bit++) {
        set_logical_pixel(column, y + bit, (bits & (1u << bit)) != 0);
    }
}

static void clear_logical_page(int page)
{
    const int y = page * 8;

    for (int x = 0; x < DISPLAY_WIDTH; x++) {
        for (int bit = 0; bit < 8; bit++) {
            set_logical_pixel(x, y + bit, false);
        }
    }
}

static void draw_char(int x, int y, char ch)
{
    const uint8_t *glyph = font[fi(ch)];

    for (int col = 0; col < 8; col++) {
        for (int bit = 0; bit < 8; bit++) {
            set_logical_pixel(x + col, y + bit,
                              (glyph[col] & (1u << bit)) != 0);
        }
    }
}

static void flush_changed(bool force)
{
    for (uint8_t page = 0; page < HW_PAGE_COUNT; page++) {
        const size_t base = (size_t)page * BSP_OLED_WIDTH;

        if (force) {
            bsp_oled_write_page(page, 0, s_fb + base, BSP_OLED_WIDTH);
            memcpy(s_shadow + base, s_fb + base, BSP_OLED_WIDTH);
            continue;
        }

        int col = 0;
        while (col < BSP_OLED_WIDTH) {
            while (col < BSP_OLED_WIDTH &&
                   s_fb[base + (size_t)col] == s_shadow[base + (size_t)col]) {
                col++;
            }

            if (col >= BSP_OLED_WIDTH) {
                break;
            }

            const int start = col;
            while (col < BSP_OLED_WIDTH &&
                   s_fb[base + (size_t)col] != s_shadow[base + (size_t)col]) {
                col++;
            }

            const size_t len = (size_t)(col - start);
            bsp_oled_write_page(page, (uint8_t)start, s_fb + base + start, len);
            memcpy(s_shadow + base + start, s_fb + base + start, len);
        }
    }
}

void display_init(void)
{
    bsp_oled_init();
    memset(s_shadow, 0xFF, sizeof(s_shadow));
    display_clear();
}

void display_clear(void)
{
    memset(s_fb, 0, sizeof(s_fb));
    flush_changed(true);
}

void display_text(int page, const char *str)
{
    if ((unsigned)page >= DISPLAY_PAGE_COUNT) {
        return;
    }

    if (!str) {
        str = "";
    }

    size_t n = strlen(str);
    if (n > DISPLAY_MAX_TEXT_CHARS) {
        n = DISPLAY_MAX_TEXT_CHARS;
    }

    clear_logical_page(page);

    for (size_t i = 0; i < n; i++) {
        draw_char((int)i * 8, page * 8, str[i]);
    }

    flush_changed(false);
}

void display_write_page(int page, int column, const uint8_t *data, size_t len)
{
    if ((unsigned)page >= DISPLAY_PAGE_COUNT || !data || len == 0) {
        return;
    }

    if (column < 0) {
        const size_t skip = (size_t)(-column);
        if (skip >= len) {
            return;
        }
        data += skip;
        len -= skip;
        column = 0;
    }

    if (column >= DISPLAY_WIDTH) {
        return;
    }

    if (len > (size_t)(DISPLAY_WIDTH - column)) {
        len = (size_t)(DISPLAY_WIDTH - column);
    }

    for (size_t i = 0; i < len; i++) {
        put_logical_page_byte(page, column + (int)i, data[i]);
    }

    flush_changed(false);
}

void display_bitmap(const uint8_t *bitmap)
{
    display_framebuffer(bitmap);
}

void display_framebuffer(const uint8_t *fb)
{
    if (!fb) {
        return;
    }

    memset(s_fb, 0, sizeof(s_fb));

    for (int page = 0; page < DISPLAY_PAGE_COUNT; page++) {
        for (int column = 0; column < DISPLAY_WIDTH; column++) {
            put_logical_page_byte(page, column,
                                  fb[(size_t)page * DISPLAY_WIDTH + column]);
        }
    }

    flush_changed(false);
}
